// Function to update metatags for a specific product
function updateProductMetatags(productId) {
    const metatag = productMetatags[productId];
    if (!metatag) return;

    // Update Open Graph tags
    document.querySelector('meta[property="og:title"]').setAttribute('content', metatag.title);
    document.querySelector('meta[property="og:description"]').setAttribute('content', metatag.description);
    document.querySelector('meta[property="og:image"]').setAttribute('content', metatag.image);

    // Update Twitter tags
    document.querySelector('meta[property="twitter:title"]').setAttribute('content', metatag.title);
    document.querySelector('meta[property="twitter:description"]').setAttribute('content', metatag.description);
    document.querySelector('meta[property="twitter:image"]').setAttribute('content', metatag.image);

    // Update page title
    document.title = metatag.title;

    // Update meta description
    document.querySelector('meta[name="description"]').setAttribute('content', metatag.description);
}

